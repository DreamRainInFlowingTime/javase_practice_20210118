/**
 * Definition for a binary tree node.
 * public class TreeNode {
 * int val;
 * TreeNode left;
 * TreeNode right;
 * TreeNode() {}
 * TreeNode(int val) { this.val = val; }
 * TreeNode(int val, TreeNode left, TreeNode right) {
 * this.val = val;
 * this.left = left;
 * this.right = right;
 * }
 * }
 */
class Solution {
    boolean flag = true;
    public boolean isSymmetric(TreeNode root) {
        return run(root,root);
    }
    boolean run(TreeNode treeA, TreeNode treeB){
        if (treeB == null && treeA == null) return true;
        else if (treeB == null || treeA == null) return false;
        return treeA.val == treeB.val && run(treeA.left,treeB.right) && run(treeA.right,treeB.left);
    }
}

class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;

    TreeNode() {
    }

    TreeNode(int val) {
        this.val = val;
    }

    TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
